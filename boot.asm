; ============================================================
; ARIONA OS - BOOTLOADER
; Реальный загрузчик для x86
; ============================================================

BITS 16
ORG 0x7C00

start:
    ; Отключаем прерывания
    cli
    
    ; Настраиваем сегменты
    xor ax, ax
    mov ds, ax
    mov es, ax
    mov ss, ax
    mov sp, 0x7C00
    
    ; Включаем прерывания
    sti
    
    ; Очищаем экран
    mov ax, 0x0003
    int 0x10
    
    ; Показываем загрузку
    mov si, msg_loading
    call print_string
    
    ; Загружаем ядро с диска
    mov ah, 0x02          ; Функция чтения
    mov al, 30            ; Читаем 30 секторов (всё ядро)
    mov ch, 0             ; Цилиндр 0
    mov cl, 2             ; Сектор 2
    mov dh, 0             ; Головка 0
    mov dl, 0x80          ; Первый жёсткий диск
    mov bx, 0x1000        ; Загружаем по адресу 0x1000
    int 0x13
    jc disk_error
    
    ; Переходим на ядро
    jmp 0x0000:0x1000

disk_error:
    mov si, msg_error
    call print_string
    jmp $

; ============================================================
; ФУНКЦИЯ ПЕЧАТИ
; ============================================================

print_string:
    lodsb
    test al, al
    jz .done
    mov ah, 0x0E
    int 0x10
    jmp print_string
.done:
    ret

; ============================================================
; ДАННЫЕ
; ============================================================

msg_loading db 'Ariona OS v2.0 - Loading kernel...', 0x0D, 0x0A, 0
msg_error   db 'Disk read error!', 0

; ============================================================
; ЗАПОЛНЯЕМ ДО 512 БАЙТ
; ============================================================

times 510 - ($ - $$) db 0
dw 0xAA55